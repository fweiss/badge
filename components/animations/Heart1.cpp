#include "Heart1.h"

Heart1::Heart1(Display &display) : Animation(display), frameIndex(0) {

}

void Heart1::drawFrame() {
    const uint16_t frameCount = 8;
    frameIndex = (frameIndex + 1) % frameCount;
    drawFrame(frameIndex);
}

void Heart1::drawFrame(uint32_t frameIndex) {
    for (int x=0; x<8; x++) {
        for (int y=0; y<8; y++) {
            const uint16_t p = y * 8 + x;
            const uint16_t t = frameIndex * 8 + y;
            uint8_t r = red[t][x];
            uint8_t g = green[t][x];
            uint8_t b = blue[t][x];
            display.setPixelRgb(p, r, g, b);
        }
    }
    display.update();
}

